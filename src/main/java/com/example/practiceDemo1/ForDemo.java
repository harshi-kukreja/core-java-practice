package com.example.practiceDemo1;

public class ForDemo {
    public static void main(String[] args) {
        for(int i=0; i<5;i++){
            System.out.println("Hello world");
        }

        //for each loop
        String[] Names={"harshi","rekha","swati","sunil"};

        for(String name : Names){
            System.out.println(name);
        }
    }


}
