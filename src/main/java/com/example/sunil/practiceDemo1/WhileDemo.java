package com.example.sunil.practiceDemo1;

public class WhileDemo {
    public static void main(String[] args) {
        int num=5;
        while (num!=0){
            System.out.println(num);
            num--;
        }
int num1=1;
        while (num1<=5){
            System.out.println(num1);
            num1++;
        }
    }
}
