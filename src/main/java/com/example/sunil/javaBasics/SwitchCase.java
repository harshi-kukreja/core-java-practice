package com.example.sunil.javaBasics;

// what will be the output of below program

public class SwitchCase {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {
            switch (i){
                case 0:
                    System.out.println("A");
                case 1:
                    System.out.println("B");
                    break;
                case 2:
                    System.out.println("C");
            }

        }
    }
}
